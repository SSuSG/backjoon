public class 기본값 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int T;
        T = sc.nextInt();

        for (int tc = 1; tc <= T; tc++) {


            System.out.println("#" + tc + " ");
        }
    }
}
